- shell
    - ターミナルをインターフェイスにして、コマンドを実行するためのプログラム
    -  OSのカーネルとユーザーをつなぐ役割を持つ
    - プログラムを実行する時はOSの機能を使う必要があり，ターミナルにプログラム実行を指示することがほとんど
        - アイコンがあるExcelのようなアプリはターミナルを利用せずに直接OSに依頼をして実行している
    - bashはshellの一種でlinuxOS用のshell
    - windowsOSはコマンドプロンプトを利用して，別のshellを実行するが，プログラムではBashOSの方が利用率が高く，WindowsでもわざわざBashを利用することは多い
    - bashではディレクトリを/区切りで表現する
    - ~はホームディレクトリを表す
